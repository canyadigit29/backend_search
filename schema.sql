CREATE OR REPLACE FUNCTION "public"."match_documents"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "user_id_filter" "uuid", "filter_document_type" "text" DEFAULT NULL::"text", "filter_start_date" "date" DEFAULT NULL::"date", "filter_end_date" "date" DEFAULT NULL::"date") RETURNS TABLE("id" "uuid", "file_id" "uuid", "content" "text", "page_number" integer, "score" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
    RETURN QUERY
    SELECT
        dc.id,
        dc.file_id,
        dc.content,
        dc.page_number,
        (1 - (dc.embedding <=> query_embedding)) AS score
    FROM
        document_chunks AS dc
    WHERE
        dc.user_id = user_id_filter
        AND (1 - (dc.embedding <=> query_embedding)) > match_threshold
        AND (filter_document_type IS NULL OR dc.document_type = filter_document_type)
        AND (filter_start_date IS NULL OR dc.meeting_date >= filter_start_date)
        AND (filter_end_date IS NULL OR dc.meeting_date <= filter_end_date)
    ORDER BY
        score DESC
    LIMIT
        match_count;
END;
$$;


ALTER FUNCTION "public"."match_documents"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer, "user_id_filter" "uuid", "filter_document_type" "text", "filter_start_date" "date", "filter_end_date" "date") OWNER TO "postgres";


